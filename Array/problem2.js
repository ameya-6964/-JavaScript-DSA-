/*  QUESTION

* Store a Set Of Words In An Array And Display The Content Both Forwards And Backwords  

*/

/* 
* SOLUTION STARTS HERE 
*/

var Name = ["Ameya","Jayesh","Tushar","Suraj","Gaurav"];

/* 
 *  DISPLAYING FORWARD DIRECTION
*/
console.log("Name In Forward Direction : " + Name);

/* 
 *  DISPLAYING REVERSE DIRECTION
*/
console.log("Name In Reverse Direction : " + Name.reverse());


/* 
  * OUTPUT
  * Name In Forward Direction : Ameya,Jayesh,Tushar,Suraj,Gaurav
  * Name In Reverse Direction : Gaurav,Suraj,Tushar,Jayesh,Ameya
  */





